# Arithmetic Operators
# Addition 
x = 12
y = 5
print("Addition", x + y)

# Subtraction
print("Sub", x - y)

# Multiplication
print("Mul", x*y)

# Divison ==> gives floor
print("Div", x/y) 

# Modulus
print("Mod", x%y)

# Exponentiation ==>> it's like 12 * 12 * 12 * 12 * 12
print("Expo", x**y)

# Floor Division ==>> rounds down to the nearest integer
print("FD", x//y)

