# Comparison Operators
# Comparison operators are used to compare two values.
# They always return either True or False.

x = 12
y = 5

# Equal to ==>> checks if both values are equal
print("Equal", x == y)

# Not equal to !=>> checks if both values are different
print("Not Equal", x != y)

# Greater than >>
print("Greater", x > y)

# Less than <<
print("Less", x < y)

# Greater than or equal to >=
print("Greater Equal", x >= y)

# Less than or equal to <=
print("Less Equal", x <= y)

a = 10
b = 10

print("a == b", a == b)
print("a != b", a != b)
print("a >= b", a >= b)
print("a <= b", a <= b)

# Comparison operators are mostly used in conditions
age = 18

print(age >= 18)   # True
print(age < 18)    # False