# integer
age = 21
print(age)
print(type(age))


# float
price = 99.99
print(price)
print(type(price))


# string
name = "Steve"
print(name)
print(type(name))


# boolean
x = True
print(x)
print(type(x))


# list
arr = [1, 2, 3, 4]
print(arr)
print(type(arr))


# tuple
cord = (10, 20)
print(cord)
print(type(cord))


# set
num = {1, 2, 3, 4}
print(num)
print(type(num))


# Dictionary
student = {
    "name": "Steve",
    "age": 21
}
print(student)
print(type(student))


# None Type
data = None
print(data)
print(type(data))